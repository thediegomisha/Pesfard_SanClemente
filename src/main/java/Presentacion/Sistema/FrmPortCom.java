    private void buscarPuertos() {
        try {
            if (chkBalanzaGrande.isSelected()) {
                cbBalanzaGrande.removeAllItems();
            } else if (chkBalanzaChica.isSelected()) {
                cbBalanzaChica.removeAllItems();
            }
            Object[] puertos = SerialPortList.getPortNames();
            for (Object puerto : puertos) {
                cbBalanzaGrande.addItem((String) puerto);
                cbBalanzaChica.addItem((String) puerto);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

